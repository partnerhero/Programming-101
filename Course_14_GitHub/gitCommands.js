/*

git commands cheat sheet:

1. Clone a repo from github:
git clone <repo URL>

2. Create your own local branch and move to it:
git checkout -b <new branch name>

3. Create a remote branch on the repo you cloned:
git push -u origin <new branch name>

4. Make all the changes you need to your code

5. Once done making changes, add them to staging area
git add .

6. Commit your changes to the branch with a message
git commit -m "your message here"

7. Push your changes to the remote branch on GitHub
git push

*/
